public class Booking {
    private String name;
    private int age;

    public Booking(String name, int age) {
        name = "Adam";
        age = 30;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getAge() {
        return this.name;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
